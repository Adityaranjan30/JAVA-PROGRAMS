public class pluspattern {
    public static void main(String[] args) {
        for(int i=1;i<=4;i++){
            System.out.println("      *");
        }
        
        for(int i=1;i<=7;i++){
            System.out.print("* ");
        }
        System.out.println();
        for(int i=1;i<=4;i++){
            System.out.println("      *");
        }
    }
    
}
